print("Pedir un número al usuario y mostrar el nombre del día al corresponde")
num1 = int(input("Ingresa un Numero del 1 al 7: "))
if num1 == 1:
    print("\nDia Lunes")
elif num1 == 2:
    print("\nDia Martes")
elif num1 == 3:
    print("\nDia Miercoles")
elif num1 == 4:
    print("\nDia Jueves")
elif num1 == 5:
    print("\nDia Viernes")
elif num1 == 6:
    print("\nDia Sabado")
elif num1 == 7:
    print("\nDia Domingo")
else :
    print("\nEl numero no esta dentro de los parametros")
print("\nFin")